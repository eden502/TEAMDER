package iob.bounderies;

public class GeneralId {
	private String domain;
	private String id;
	
	
	public GeneralId() {
		
	}


	public String getDomain() {
		return domain;
	}


	public void setDomain(String domain) {
		this.domain = domain;
		
	}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
		
	}
}
