package bounderies;

public class ActivityId {

	private String domain;
	private String id;
	
	
	public ActivityId() {
	}
	
	public String getDomain() {
		return domain;
	}
	public ActivityId setDomain(String domain) {
		this.domain = domain;
		return this;
	}
	public String getId() {
		return id;
	}
	public ActivityId setId(String id) {
		this.id = id;
		return this;
	}
	
	
	
}
