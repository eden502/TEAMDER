package bounderies;

public class UserId {

	private String domain;
	private String email;
	
	
	public UserId() {
		super();
	}


	public String getDomain() {
		return domain;
	}


	public UserId setDomain(String domain) {
		this.domain = domain;
		return this;
	}


	public String getEmail() {
		return email;
	}


	public UserId setEmail(String email) {
		this.email = email;
		return this;
	}
	
	
	
	
}
